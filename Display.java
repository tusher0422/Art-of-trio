public class Display {
    void print(){
        System.out.println("1      3");
        System.out.println();
        System.out.println("   2    ");
        System.out.println();
        System.out.println("4      5");
    }
    void print1(){
        System.out.println("1-----3");
        System.out.println(" \\  / |");
        System.out.println("   2  |");
        System.out.println("  / \\ |");
        System.out.println("4     5");
    }
    void print4(){
        System.out.println("1 \t   \t 3");
        System.out.println("  \t 2 \t  ");
        System.out.println("4 \t   \t 6");
        System.out.println("  \t 5 \t ");

    }
    void print5(){
        System.out.println("1-------3");
        System.out.println(" \\    / "); 
        System.out.println("   2 /");
        System.out.println("  /|/\\ ");
        System.out.println(" |/|  \\ ");
        System.out.println(" 4 |   6");
        System.out.println(" \\ |  "); 
        System.out.println("   5 ");
    }
}
